def main():
    configs = get_server_configs()

    for config in configs:
        name = config['folder']
        config = config['config']
        begin_server (config, name)

    threading.Thread(target=async_output_server_info, daemon=True).start()

    while True:
        time.sleep(3)

if __name__ == "__main__":
    main()
